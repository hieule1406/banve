                    function loadAjaxNoti(){
                        $('.loader-inner').loaders();
                        var template = $.templates('#noti_template');
                        var $this = this;
                        if($this.ajax){
                            return false;
                            //this.ajax.abort();
                        }
                        $.ajax({
                            url: 'user/notification/loadAjaxNoti',
                            type: 'post',
                            dataType: 'json',
                            data: {

                            },
                            success: function(result){
                                if(result.check == 'ok') {
                                    socket.emit('read_noti',result.check);
                                    //var htmlOutput = template.render(result);
                                    if(result.noti.length > 0) {
                                        var markup = '';

                                        $.each(result.noti , function(key,value){
                                            markup +='<li class="noti noti-comment">'+value.content+'</li>';
                                        });
                                        markup += '<li class="noti noti-more text-center"><a href="user/profile/notifications">Xem tất cả thông báo</a></li>';
                                        $('#user-noti-drop').html(markup);
                                    } else {
                                        $('#user-noti-drop').html('<p class="text-center pdt05">Bạn không có thông báo nào</p>');
                                    }
                                    $('#count_new_noti').html('');
                                }
                            },
                            complete: function(){
                                $this.ajax = null;
                            }
                        });
                    }

